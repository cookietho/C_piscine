int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		rc;

	i = 0;
	rc = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			rc++;
		i++;
	}
	return (rc);
}
