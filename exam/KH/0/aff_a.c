#include <unistd.h>

char	aff_a(char *str)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (str[i] != '\0')
	{
		if (str[i] == 'a')
			a = 1;
		i++;
	}
	if (a == 1)
		write(1, "a\n", 2);
	else
	{
		write(1, "\n", 1);
	}
	return (0);
}

int		main(int ac, char *av[])
{
	if (ac == 2)
		aff_a(av[1]);
	if (ac != 2)
		write(1, "a\n", 2);
	return (0);
}
