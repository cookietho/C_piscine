#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int c;
	
	c = *b;
	*b = *a;
	*a = c;
}

int		main()
{
	int a;
	int b;

	a = 13;
	b = 5;
	ft_swap(&a,&b);
	printf("%d\n",a);
	return (0);
}
