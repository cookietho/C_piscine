int		max(int* tab, unsigned int len)
{
	unsigned int i;
	int a;

	i = 0;
	a = tab[i];
	if(tab[i] == '\0')
		return (0);
	while (i < len)
	{
		if(a < tab[i])
			a = tab[i];
		i++;
	}
	return (a);
}
