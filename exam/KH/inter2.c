#include <unistd.h>

void	inter(char *s1, char *s2)
{
	int i, j, k;

	i = 0;
	while(s1[i] != '\0')
	{
		k = 0;
		j = 0;
		while(j < i)
		{	
			if(s1[i] == s1[j])
				i++;
			j++; 
		}
		while(s2[k] != '\0' && k != -1)
		{
			if(s1[i] == s2[k])
			{
				write(1, &s1[i], 1);
				k = -1;
			}
			else
				k++;
		}
		i++;
	}
}

int		main(int ac, char **av)
{
	if(ac == 3)
		inter(av[1], av[2]);
	write(1, "\n", 1);
}
