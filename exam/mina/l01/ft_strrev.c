char	*ft_strrev(char *str)
{
	char t;
	int n;
	int i;

	i = 0;
	n = 0;
	while (str[n] != '\0')
		n++;
	n--;
	while (i < n)
	{
		t = str[n];
		str[n] = str[i];
		str[i] = t;
		i++;
		n--;
	}
	return (str);
}
